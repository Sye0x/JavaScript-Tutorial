// Arithmetic Operators
// ( + , - , * , / , ** . % )

let num = 5;
num += 1;
console.log(num);

num = 5;
num -= 1;
console.log(num);

num = 5;
num *= 2;
console.log(num);

num = 10;
num /= 2;
console.log(num);

num = 10;
num **= 2;
console.log(num);

num = 10;
num %= 3;
console.log(num);
// increment
num = 10;
num++;
console.log(num);
// decrement
num = 10;
num--;
console.log(num);

