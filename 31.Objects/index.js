// Define an object 'pikachu' with properties and a method
const pikachu = {
  name: "Pikachu", // Name of the character
  type: "Electric", // Type of the character
  usethunderbolt: () => {
    console.log("Pikachuuuu"); // Method that prints "Pikachuuuu" to the console
  },
};

// Log the name of 'pikachu' to the console
console.log(pikachu.name);

// Log the type of 'pikachu' to the console
console.log(pikachu.type);

// Call the 'usethunderbolt' method of 'pikachu'
pikachu.usethunderbolt();
